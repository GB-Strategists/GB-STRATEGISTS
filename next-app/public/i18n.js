class I18nContext {
  constructor() {
    this.langs = { en: enTranslations, es: {} }; // ES is original
    this.currentLang = localStorage.getItem('gb_lang') || 'es';
    this.textNodes = [];
  }

  init() {
    // Traverse DOM to cache text nodes
    const walk = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    let n;
    while(n = walk.nextNode()) {
      let val = n.nodeValue;
      if(val.trim().length > 1) {
          // Normalize spaces for dictionary lookup
          let baseKey = val; // We will use exact match to keep whitespace intact
          
          let lookupKey = val.trim(); 
          let foundKey = undefined;
          
          // Slow but exact dictionary lookup to handle spacing differences if any
          let match = Object.keys(this.langs.en).find(k => k === lookupKey || k === val);
          
          if(match) {
            this.textNodes.push({
                node: n,
                es: val,
                en: val.replace(match, this.langs.en[match])
            });
          }
      }
    }
    this.applyLang();
  }

  setLang(lang) {
    this.currentLang = lang;
    localStorage.setItem('gb_lang', lang);
    this.applyLang();
    // Update active button classes
    document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
    document.getElementById('lang-' + lang)?.classList.add('active');
  }

  applyLang() {
    this.textNodes.forEach(item => {
      item.node.nodeValue = this.currentLang === 'en' ? item.en : item.es;
    });
    
    // Support translated modal data!
    if(window.i18nModalTranslate) {
      window.i18nModalTranslate(this.currentLang);
    }
  }
}

// Ensure the Context API is initialized when DOM is ready
document.addEventListener("DOMContentLoaded", () => {
    window.gbI18n = new I18nContext();
    window.gbI18n.init();
    
    // Override setLanguage to use the context
    window.setLanguage = (lang) => window.gbI18n.setLang(lang);
    
    // Sync initial active button
    document.querySelectorAll('.lang-btn').forEach(b => b.classList.remove('active'));
    const initialBtn = document.getElementById('lang-' + window.gbI18n.currentLang);
    if(initialBtn) initialBtn.classList.add('active');
});
